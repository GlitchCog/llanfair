package com.jenmaarai.llanfair.model;

import javax.swing.Icon;

/**
 * A counter made to keep track of a given element during a run. A counter 
 * starts with a value specified by the user and is incremented by a step value
 * every time it is activated. The step value can be positive or negative.
 */
public class Counter {
    
    private Icon icon;
    private String name;
    private int start;
    private int step;
    private int value;

}
